// 01 - Script Console Log

// console.log writes code to the terminal or browser's console
console.log("I'm sending you a message in the console!")
// console.table creates a table in the console
// This can be done by creating an array that relates each value to an index position(starting with 0)
console.table(["value 1", "value 2", "value 3"])
//  or a table be created as key:value pairs
console.table({"col 1": "value 1", "col 2": "value 2", "col 3": "value 3"})

