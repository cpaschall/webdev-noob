// 03 - Hello Variable

// the 'var' (variable) keyword is used to bind a value to a designated name
var myName = "Cal"
var myAge = 38
console.log("My name is " + myName + " and I am " + myAge + " years old.") 
// variable assigments can be changed after they are declared
var myName = "James"
var myAge = 22
console.log("My name is " + myName + " and I am " + myAge + " years old.") 

//  ------------------------------------------------------------------ //

// 05 - Primitive Types

// Primitive data types include undefined, string, number and boolean
// Undefined are variables that have not been defined
console.log("Undefined variable:")
var myUndefined
console.log(myUndefined)
// Number variables can be integers or decimals(floats)
console.log("Number variables:")
var myInt = 23
var myFloat = 45.97
console.log("Integer: " + myInt)
console.log("Float: " + myFloat)
// Booleans have two values: true and false
var isBooleanTrue = true
var isBooleanFalse = false
console.log("TRUE/FALSE: The sky is blue: " + isBooleanTrue)
console.log("TRUE/FALSE: Pigs can fly: " + isBooleanTrue)

